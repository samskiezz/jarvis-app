# 3. TF-IDF term weighting
# ---------------------------------------------------------------------------
def tfidf_weights(documents: list[str] | None = None) -> dict:
    """Compute TF-IDF weights over a small corpus.

    tf(t,d) = count(t,d) / len(d)
    idf(t)  = ln(N / df(t))   (df = number of docs containing t)
    KNOWN behaviour: a term appearing in only one document (rare) receives a
    higher IDF -> higher TF-IDF weight than a term appearing in every document
    (which gets idf = ln(N/N) = 0).
    """
    if documents is None:
        documents = [
            "the cat sat",
            "the dog ran",
            "the bird flew unique",
        ]
    N = len(documents)
    tokenized = [_tokenize(d) for d in documents]

    df: Counter = Counter()
    for toks in tokenized:
        for t in set(toks):
            df[t] += 1

    tfidf: list[dict[str, float]] = []
    for toks in tokenized:
        counts = Counter(toks)
        total = len(toks) or 1
        weights = {}
        for t, c in counts.items():
            tf = c / total
            idf = math.log(N / df[t])
            weights[t] = tf * idf
        tfidf.append(weights)

    # "the" appears in every doc -> idf 0; "unique" appears in 1 doc -> high idf.
    common_term = "the"
    rare_term = "unique"
    common_weight = max(d.get(common_term, 0.0) for d in tfidf)
    rare_weight = max(d.get(rare_term, 0.0) for d in tfidf)

    return {
        "method": "tfidf_weights",
        "n_docs": int(N),
        "idf": {t: float(math.log(N / c)) for t, c in df.items()},
        "tfidf": [{k: float(v) for k, v in d.items()} for d in tfidf],
        "common_term": common_term,
        "common_weight": float(common_weight),
        "rare_term": rare_term,
        "rare_weight": float(rare_weight),
        "rare_weighted_higher": bool(rare_weight > common_weight),
        **SIMULATION,
    }


# ---------------------------------------------------------------------------
# 4. Levenshtein edit distance
# ---------------------------------------------------------------------------
def levenshtein_distance(a: str = "kitten", b: str = "sitting") -> dict:
    """Classic dynamic-programming Levenshtein (insert/delete/substitute = 1).

    KNOWN: distance("kitten", "sitting") = 3.
    """
    m, n = len(a), len(b)
    # dp[i][j] = edit distance between a[:i] and b[:j].
    dp = np.zeros((m + 1, n + 1), dtype=int)
    dp[:, 0] = np.arange(m + 1)
    dp[0, :] = np.arange(n + 1)
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            cost = 0 if a[i - 1] == b[j - 1] else 1
            dp[i, j] = min(
                dp[i - 1, j] + 1,        # deletion
                dp[i, j - 1] + 1,        # insertion
                dp[i - 1, j - 1] + cost  # substitution / match
            )
    distance = int(dp[m, n])
    norm = distance / max(m, n) if max(m, n) else 0.0
    return {
        "method": "levenshtein_distance",
        "a": a,
        "b": b,
        "distance": distance,
        "normalized": float(norm),
        **SIMULATION,
    }


