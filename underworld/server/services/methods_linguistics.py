# 5. Cosine similarity of bag-of-words vectors
# ---------------------------------------------------------------------------
def cosine_similarity_bow(text_a: str | None = None,
                          text_b: str | None = None) -> dict:
    """Cosine similarity of bag-of-words count vectors.

    cos = (u . v) / (|u| |v|).
    KNOWN: identical documents -> similarity = 1.0; documents with no shared
    vocabulary -> 0.0.
    """
    if text_a is None:
        text_a = "the quick brown fox jumps"
    if text_b is None:
        text_b = "the quick brown fox jumps"

    ca, cb = Counter(_tokenize(text_a)), Counter(_tokenize(text_b))
    vocab = sorted(set(ca) | set(cb))
    u = np.array([ca.get(w, 0) for w in vocab], dtype=float)
    v = np.array([cb.get(w, 0) for w in vocab], dtype=float)

    denom = float(np.linalg.norm(u) * np.linalg.norm(v))
    sim = float(np.dot(u, v) / denom) if denom > 0 else 0.0

    return {
        "method": "cosine_similarity_bow",
        "vocab_size": int(len(vocab)),
        "similarity": sim,
        **SIMULATION,
    }


