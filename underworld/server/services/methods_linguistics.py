# 7. Heaps' law vocabulary growth: V = K * N**beta
# ---------------------------------------------------------------------------
def heaps_law_fit(text: str | None = None, n_points: int = 50) -> dict:
    """Fit Heaps' law V = K * N**beta by least squares in log-log space.

    We accumulate vocabulary size V against tokens seen N, then fit
    log V = log K + beta * log N.
    KNOWN: for natural-language text beta falls in 0.4-0.6. We synthesise a
    Zipfian-distributed token stream (which provably produces a sublinear
    Heaps exponent in that band) when no text is supplied.
    """
    if text is None:
        # Generate tokens by sampling word ranks from a Zipf distribution;
        # Zipfian sampling yields Heaps exponent beta ~ 0.5.
        rng = np.random.default_rng(42)
        # 40000 tokens drawn from a Zipf(a=1.9) law over word ids; the steep
        # exponent saturates the vocabulary into the sublinear Heaps regime
        # (beta ~ 0.5, the natural-language band).
        samples = rng.zipf(1.9, size=40000)
        tokens = [str(s) for s in samples]
    else:
        tokens = _tokenize(text)

    n_total = len(tokens)
    if n_total < 10:
        raise ValueError("need at least 10 tokens for a Heaps fit")

    # Sample N values logarithmically from 1 to n_total.
    Ns = np.unique(np.geomspace(2, n_total, n_points).astype(int))
    seen: set[str] = set()
    Vs = []
    last = 0
    for N in Ns:
        for tok in tokens[last:N]:
            seen.add(tok)
        last = N
        Vs.append(len(seen))
    Vs = np.array(Vs, dtype=float)

    log_N = np.log(Ns.astype(float))
    log_V = np.log(Vs)
    beta, log_K = np.polyfit(log_N, log_V, 1)
    K = float(math.exp(log_K))

    return {
        "method": "heaps_law_fit",
        "K": K,
        "beta": float(beta),
        "n_tokens": int(n_total),
        "vocab_size": int(len(seen)),
        "beta_in_natural_band": bool(0.4 <= beta <= 0.6),
        **SIMULATION,
    }


