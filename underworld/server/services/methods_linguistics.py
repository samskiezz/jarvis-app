# 2. n-gram language-model perplexity
# ---------------------------------------------------------------------------
def ngram_perplexity(train_text: str | None = None,
                     test_text: str | None = None,
                     n: int = 2,
                     vocab_smoothing: float = 1.0) -> dict:
    """Train an n-gram (default bigram) model with add-k (Laplace) smoothing
    and compute perplexity of test text.

    Perplexity = exp(-(1/M) * sum log P(w_i | context)).
    KNOWN behaviour: text drawn from the training distribution yields a lower
    perplexity than out-of-distribution text. We return perplexity for an
    in-distribution probe and an out-of-distribution probe for comparison.
    """
    if train_text is None:
        train_text = ("the cat sat on the mat the cat ate the fish "
                      "the dog sat on the log the dog ate the bone ") * 5
    train = _tokenize(train_text)

    # Build (n-1)-gram context counts and n-gram counts.
    context_counts: Counter = Counter()
    ngram_counts: Counter = Counter()
    for i in range(len(train) - n + 1):
        gram = tuple(train[i:i + n])
        ngram_counts[gram] += 1
        context_counts[gram[:-1]] += 1
    vocab = set(train)
    V = len(vocab)

    def _perplexity(tokens: list[str]) -> float:
        if len(tokens) < n:
            return float("inf")
        log_prob = 0.0
        count = 0
        for i in range(len(tokens) - n + 1):
            gram = tuple(tokens[i:i + n])
            ctx = gram[:-1]
            num = ngram_counts.get(gram, 0) + vocab_smoothing
            den = context_counts.get(ctx, 0) + vocab_smoothing * V
            prob = num / den
            log_prob += math.log(prob)
            count += 1
        return math.exp(-log_prob / count)

    in_dist = test_text if test_text is not None else "the cat sat on the mat"
    out_dist = "quantum helicopter avalanche purple xylophone glitch"

    ppl_in = _perplexity(_tokenize(in_dist))
    ppl_out = _perplexity(_tokenize(out_dist))

    return {
        "method": "ngram_perplexity",
        "n": int(n),
        "vocab_size": int(V),
        "perplexity_in_distribution": float(ppl_in),
        "perplexity_out_of_distribution": float(ppl_out),
        "in_lower_than_out": bool(ppl_in < ppl_out),
        **SIMULATION,
    }


