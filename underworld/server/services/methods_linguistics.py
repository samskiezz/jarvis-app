"""Real computational-linguistics methods (in-world simulations).

Eight genuine, named algorithms from NLP / information theory — not stubs or
external connectors. Each is implemented with numpy / collections and is
verified against a KNOWN ground-truth value in the test suite:

  1. Zipf's law rank-frequency fit   -> log-log slope ~= -1 on Zipfian data
  2. n-gram language-model perplexity -> lower perplexity for in-distribution text
  3. TF-IDF term weighting            -> rare term weighted higher than common
  4. Levenshtein edit distance        -> "kitten"->"sitting" = 3
  5. Cosine similarity (bag-of-words) -> identical documents = 1.0
  6. Shannon entropy / bits-per-char  -> English raw ~= 4 bits/char
  7. Heaps' law vocabulary growth     -> V = K * N**beta, beta in 0.4-0.6
  8. BLEU score n-gram overlap        -> perfect match = 1.0

All functions return plain dicts of JSON-serialisable values.
"""
from __future__ import annotations

import math
import re
from collections import Counter

import numpy as np

SIMULATION = {"simulation": True, "physical_hardware": False}


def _tokenize(text: str) -> list[str]:
    """Lowercase word tokenizer (alphanumeric runs)."""
    return re.findall(r"[a-z0-9]+", text.lower())


# ---------------------------------------------------------------------------
# 1. Zipf's law rank-frequency fit
# ---------------------------------------------------------------------------
def zipf_law_fit(frequencies: list[int] | None = None) -> dict:
    """Fit Zipf's law: frequency ~ rank ** (-s) by least squares in log-log.

    On data generated by f(r) = C / r (an ideal Zipfian distribution) the
    log-log slope equals -s = -1 (KNOWN). We synthesise such data when no
    frequencies are supplied so the verification is exact.

    Returns the fitted slope (which should be ~ -1) and exponent s.
    """
    if frequencies is None:
        # Ideal Zipfian: f(r) = N / r  with N = 100000, ranks 1..1000.
        ranks = np.arange(1, 1001)
        frequencies = (100000.0 / ranks).tolist()

    freqs = np.asarray(sorted((float(f) for f in frequencies), reverse=True))
    ranks = np.arange(1, len(freqs) + 1, dtype=float)

    log_r = np.log(ranks)
    log_f = np.log(freqs)
    # Linear fit log_f = slope * log_r + intercept.
    slope, intercept = np.polyfit(log_r, log_f, 1)

    # Coefficient of determination.
    pred = slope * log_r + intercept
    ss_res = float(np.sum((log_f - pred) ** 2))
    ss_tot = float(np.sum((log_f - log_f.mean()) ** 2))
    r_squared = 1.0 - ss_res / ss_tot if ss_tot > 0 else 1.0

    return {
        "method": "zipf_law_fit",
        "slope": float(slope),
        "exponent_s": float(-slope),
        "intercept": float(intercept),
        "r_squared": float(r_squared),
        "n_ranks": int(len(freqs)),
        **SIMULATION,
    }


