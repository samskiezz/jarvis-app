# 8. BLEU score n-gram overlap
# ---------------------------------------------------------------------------
def bleu_score(candidate: str | None = None,
               reference: str | None = None,
               max_n: int = 4) -> dict:
    """Sentence-level BLEU with modified n-gram precision and brevity penalty.

    BLEU = BP * exp( (1/N) * sum_n log p_n )
      p_n = clipped n-gram precision
      BP  = 1 if c > r else exp(1 - r/c)
    KNOWN: a candidate identical to the reference scores 1.0.
    """
    if candidate is None:
        candidate = "the cat sat on the mat"
    if reference is None:
        reference = "the cat sat on the mat"

    cand = _tokenize(candidate)
    ref = _tokenize(reference)
    c, r = len(cand), len(ref)

    precisions = []
    for k in range(1, max_n + 1):
        cand_grams = Counter(tuple(cand[i:i + k]) for i in range(len(cand) - k + 1))
        ref_grams = Counter(tuple(ref[i:i + k]) for i in range(len(ref) - k + 1))
        if not cand_grams:
            precisions.append(0.0)
            continue
        clipped = sum(min(cnt, ref_grams.get(g, 0)) for g, cnt in cand_grams.items())
        total = sum(cand_grams.values())
        precisions.append(clipped / total)

    if min(precisions) > 0:
        log_mean = sum(math.log(p) for p in precisions) / len(precisions)
        geo_mean = math.exp(log_mean)
    else:
        geo_mean = 0.0

    # Brevity penalty.
    if c == 0:
        bp = 0.0
    elif c > r:
        bp = 1.0
    else:
        bp = math.exp(1.0 - r / c)

    bleu = bp * geo_mean

    return {
        "method": "bleu_score",
        "precisions": [float(p) for p in precisions],
        "brevity_penalty": float(bp),
        "bleu": float(bleu),
        "candidate_len": int(c),
        "reference_len": int(r),
        **SIMULATION,
    }


