# 6. Shannon entropy / bits-per-character
# ---------------------------------------------------------------------------
def char_entropy(text: str | None = None) -> dict:
    """First-order Shannon entropy of the character distribution.

    H = -sum p(c) * log2 p(c)   bits per character.
    KNOWN: raw English text (no context model) has a per-character entropy of
    roughly 4 bits/char (the empirical first-order figure; the theoretical
    uniform maximum over 26 letters is log2(26) ~= 4.7). We default to a chunk
    of English prose, whose measured entropy lands in the ~4 bit/char band.
    """
    if text is None:
        text = (
            "the quick brown fox jumps over the lazy dog while the "
            "early morning sunlight streams across the quiet meadow and "
            "the gentle breeze carries the scent of fresh rain over the "
            "rolling hills where sheep graze beneath the open sky"
        )
    # Letters only, lowercased -> models the classic 26/27-symbol alphabet.
    chars = [c for c in text.lower() if c.isalpha()]
    total = len(chars)
    counts = Counter(chars)
    probs = np.array([c / total for c in counts.values()], dtype=float)
    entropy_bits = float(-np.sum(probs * np.log2(probs)))
    max_entropy = float(math.log2(len(counts))) if counts else 0.0

    return {
        "method": "char_entropy",
        "n_chars": int(total),
        "n_symbols": int(len(counts)),
        "bits_per_char": entropy_bits,
        "max_bits_per_char": max_entropy,
        "redundancy": float(1.0 - entropy_bits / max_entropy) if max_entropy else 0.0,
        **SIMULATION,
    }


